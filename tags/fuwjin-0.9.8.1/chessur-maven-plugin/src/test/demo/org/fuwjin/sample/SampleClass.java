package org.fuwjin.sample;

public class SampleClass {
   private final int id;
   private final String name;

   public SampleClass(final int id, final String name) {
      this.id = id;
      this.name = name;
   }

   public int getId() {
      return id;
   }

   public String getName() {
      return name;
   }
}
